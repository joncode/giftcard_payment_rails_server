class GiftAdmin < Gift

    def self.create args={}
        resp = super
        if resp
            messenger(resp)
        end
        resp
    end

    def self.messenger(resp)
        if resp.payable.success?
            Relay.send_push_notification(resp)
            puts "GiftAdmin -messenger- Notify Receiver via email #{resp.receiver_name}"
            resp.notify_receiver
        end
    end
    
private

    def pre_init args={}
        shoppingCart = JSON.parse args["shoppingCart"]
        args["cat"]     = set_cat(args)
        args["value"]   = calculate_value(shoppingCart)
        args["cost"]    = calculate_cost(shoppingCart)
        giver = args["giver"]
        args["payable"] = giver.new_debt(args["value"])
    end

    def post_init args={}
        puts "NOTIFY RECEIVER VIA #{self.receiver_email}"
    end

    def calculate_value shoppingCart
        shoppingCart.sum {|z| z["price"].to_i * z["quantity"].to_i }
    end

    def calculate_cost shoppingCart
        shoppingCart.sum {|z| z["price_promo"].to_f * z["quantity"].to_i }
    end

    def set_cat args
        if args["cat"] && args["cat"].class == Fixnum
            args["cat"]
        else
            100
        end
    end

